package com.thais.cursojava.aula11;

public class VariáveisPontoFlutuante {

	public static void main(String[] args) {
		
		double valorPassagem = 2.90;
		
		float valorTomate = 3.95f; //o valor padrão do ponto flutuante é o double por isso o f no final para converter o valor em float
		
		System.out.println("Valor da Passagem: " + valorPassagem);
		System.out.println("Valor do Tomate: " + valorTomate);
	}

}
